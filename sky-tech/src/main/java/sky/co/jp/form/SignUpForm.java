package sky.co.jp.form;

public class SignUpForm {
    String id;
    String mail;
    String name;
    String sex;
    String status;
    String genbanName;
    String standardTime;
    String upperTime;
    String password;
    String passwordAgain;
    String itType;
    String genbanAddress;
    String remarks;
    String passwordOld;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getGenbanName() {
        return genbanName;
    }

    public void setGenbanName(String genbanName) {
        this.genbanName = genbanName;
    }

    public String getStandardTime() {
        return standardTime;
    }

    public void setStandardTime(String standardTime) {
        this.standardTime = standardTime;
    }

    public String getUpperTime() {
        return upperTime;
    }

    public void setUpperTime(String upperTime) {
        this.upperTime = upperTime;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPasswordAgain() {
        return passwordAgain;
    }

    public void setPasswordAgain(String passwordAgain) {
        this.passwordAgain = passwordAgain;
    }

    public String getItType() { return itType; }

    public void setItType(String itType) { this.itType = itType; }

    public String getGenbanAddress() { return genbanAddress; }

    public void setGenbanAddress(String genbanAddress) { this.genbanAddress = genbanAddress; }

    public String getRemarks() { return remarks; }

    public void setRemarks(String remarks) { this.remarks = remarks; }

    public String getPasswordOld() {
        return passwordOld;
    }

    public void setPasswordOld(String passwordOld) {
        this.passwordOld = passwordOld;
    }
}
