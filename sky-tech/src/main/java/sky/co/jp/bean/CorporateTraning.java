package sky.co.jp.bean;

public class CorporateTraning {

    String corporateId;
    String corporateTrainingName;
    String corporateTrainingStartDate;
    String corporateTrainingStatus;

    public String getCorporateId() {
        return corporateId;
    }

    public void setCorporateId(String corporateId) {
        this.corporateId = corporateId;
    }

    public String getCorporateTrainingName() {
        return corporateTrainingName;
    }

    public void setCorporateTrainingName(String corporateTrainingName) {
        this.corporateTrainingName = corporateTrainingName;
    }

    public String getCorporateTrainingStartDate() {
        return corporateTrainingStartDate;
    }

    public void setCorporateTrainingStartDate(String corporateTrainingStartDate) {
        this.corporateTrainingStartDate = corporateTrainingStartDate;
    }

    public String getCorporateTrainingStatus() {
        return corporateTrainingStatus;
    }

    public void setCorporateTrainingStatus(String corporateTrainingStatus) {
        this.corporateTrainingStatus = corporateTrainingStatus;
    }
}
