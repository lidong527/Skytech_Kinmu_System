CREATE TABLE `m_account` (
  `USER_MAIL` varchar(255) NOT NULL COMMENT 'アカウントのユーザー名',
  `PASSWORD` varchar(255) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT NULL COMMENT 'アカウントのパスワード',
